package basic;

public class FloatExample {

	public static void main(String[] args) {
		
		float f1 = 7.12345f;
		//실수 리터럴의 기본 데이터 타입은 double이다. 그래서 float에 f를 붙임.
		//소수점 7자리까지 출력가능
		
		double d1 = 7.12345;
		//소수점 15자리까지 출력가능
		////////////////////////////////////////////
		
		float f2 = 1.234567891234f;
		double d2 = 1.234567891234;
		
		
		System.out.println(f2);
		System.out.println(d2);
	}
}
